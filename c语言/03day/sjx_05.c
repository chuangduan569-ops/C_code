#include<stdio.h>
int main(void)
{
	int i = 0, j = 0;
	int n = 0;
	scanf("%d",&n);
	for(i = 1;i <= n; i++)
	{
		for(j = 1;j <= n-i; j++)
		{
		printf(" ");
		}
		for(j = 1;j <= 2*i-1; j++){
			printf("*");
		}
		printf("\n");
	}
/*
1. 外层循环：for(i = 1; i <= n; i++)

i=1：第1行

i=2：第2行

...

i=5：第5行

2. 空格循环：for(j = 1; j <= n - i; j++)

第1行：n-i = 5-1 = 4 → 打印4个空格

第2行：n-i = 5-2 = 3 → 打印3个空格

第3行：n-i = 5-3 = 2 → 打印2个空格

第4行：n-i = 5-4 = 1 → 打印1个空格

第5行：n-i = 5-5 = 0 → 打印0个空格

3. 星号循环：for(j = 1; j <= 2*i - 1; j++)

关键公式：2*i - 1 → 产生奇数序列

第1行：2*1-1 = 1 → 1个星号

第2行：2*2-1 = 3 → 3个星号

第3行：2*3-1 = 5 → 5个星号

第4行：2*4-1 = 7 → 7个星号

第5行：2*5-1 = 9 → 9个星号
 */
}
