#include<stdio.h>
int main(void)
{
	int i = 0, j = 0;
	int n = 0;
	scanf("%d",&n);
	for(i = n;i >= 1; i--)
	{
		for(j = 1;j <= n-i; j++)
		{
		printf(" ");
		}
		for(j = 1;j <= 2*i-1; j++){
			printf("*");
		}
		printf("\n");
	}
/*
1. 外层循环：for(i = n; i >= 1; i--)

从最大到最小递减：i=5,4,3,2,1

2. 空格循环：for(j = 1; j <= n - i; j++)

第1行（i=5）：n-i = 5-5 = 0 → 0个空格

第2行（i=4）：n-i = 5-4 = 1 → 1个空格

第3行（i=3）：n-i = 5-3 = 2 → 2个空格

第4行（i=2）：n-i = 5-2 = 3 → 3个空格

第5行（i=1）：n-i = 5-1 = 4 → 4个空格

3. 星号循环：for(j = 1; j <= 2*i - 1; j++)

同样用 2*i - 1，但i递减

第1行（i=5）：2*5-1 = 9 → 9个星号

第2行（i=4）：2*4-1 = 7 → 7个星号

第3行（i=3）：2*3-1 = 5 → 5个星号

第4行（i=2）：2*2-1 = 3 → 3个星号

第5行（i=1）：2*1-1 = 1 → 1个星号

4. 特点：

星号数递减：9,7,5,3,1

空格数递增：0,1,2,3,4

这是正金字塔的倒序版本
*/
}
