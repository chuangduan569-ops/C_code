#ifndef __TBF_H
#define __TBF_H

/*
 * 令牌桶头文件
 * 功能：提供令牌桶流量控制功能
 * 作者：LogHawk
 * 日期：2026-04-02
 */

/*
 * 函数名称：tbf_init
 * 功能描述：初始化一个令牌桶
 * 参数说明：
 *     cps   - 客户指定的速率（每秒生成的令牌数）
 *     burst - 客户指定的容量（桶的最大容量）
 * 返回值：
 *     成功返回令牌桶库的下标(>=0)
 *     失败返回负数(<0)
 *     -1 : cps或burst参数无效（<=0）
 *     -2 : 令牌桶库已满（最多1024个桶）
 *     -3 : 内存分配失败
 */
int tbf_init(int cps, int burst);

/*
 * 函数名称：tbf_fetch_token
 * 功能描述：从令牌桶获取令牌（阻塞版本）
 *           如果令牌不足，会阻塞等待直到有令牌可用
 * 参数说明：
 *     td - 令牌桶库的描述符（由tbf_init返回的令牌桶ID）
 *     n  - 客户要取走的令牌数
 * 返回值：
 *     成功返回实际取走的令牌数（可能小于n）
 *     失败返回负数
 *     -1 : td或n参数无效
 *     -2 : td对应的令牌桶不存在
 */
int tbf_fetch_token(int td, int n);


/*
 * 函数名称：tbf_get_tokens
 * 功能描述：获取当前令牌桶中的令牌数（用于调试和监控）
 * 参数说明：
 *     td - 令牌桶库的描述符（由tbf_init返回的令牌桶ID）
 * 返回值：
 *     成功返回当前令牌桶中的令牌数
 *     失败返回-1
 */
int tbf_get_tokens(int td);

/*
 * 函数名称：tbf_destroy
 * 功能描述：销毁相应的令牌桶，释放内存
 * 参数说明：
 *     td - 令牌桶库的描述符（由tbf_init返回的令牌桶ID）
 * 返回值：
 *     成功返回0
 *     失败返回负数
 *     -1 : td参数无效
 *     -2 : td对应的令牌桶不存在
 */
int tbf_destroy(int td);

#endif
